#sum of 2 numbers
f=lambda num1,num2:num1+num2
print(f(10,20))
print(f(50,150))

#multiply 3 numbers
f=lambda num1,num2,num3:num1*num2*num3
print(f(10,20,30))

#cube of a number
f=lambda num:num**3
print(f(2))
